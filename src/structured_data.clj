(defn spiff [v]
  ( let [lhs (get v 0)
         rhs (get v 2)]
    (+ (if (nil? lhs) 0 lhs)
       (if (nil? rhs) 0 rhs))))

(defn cutify [v]
  (conj v "<3"))

(defn spiff-destructuring [v]
  (let [[lhs _ rhs] v]
    (+ lhs rhs)))

